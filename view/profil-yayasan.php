 <div class="page-head-line"><h2>Visi,Misi & Strategi</h2></div>
              
<p><strong><h4>VISI</h4></strong></p>
<p>-Menjadi lembaga yang sensitif, proaktif, dan produktif dalam meningkatkan kesejahteraan umat, baik spiritual maupun material.</p>
<p><strong><h4>MISI</h4></strong></p>
<p>-Melakukan pembinaan yang terstruktur dan kontinyu melalui pendidikan, dakwah dan pelatihan.</p>
<p><strong><h4>Strategi</h4></strong></p>
<p>-Membuat lembaga pendidikan formal untuk usia sekolah; TK, SD, SMP, SMU dan PT.</p>
<p>-Membuat lembaga lembaga pendidikan informal untuk orang tua siswa, dan masyarakat (untuk pembinaan keagamaan, kursus atau pelatihan tentang kewirausahaan dan teknologi tepat guna, dan sejenisnya).</p>
<p>-Menyelenggarakan berbagai unit usaha sebagai penopang penyelenggaraan pendidikan formal dan informal.</p>
<p>-Menyelenggarakan berbagai unit usaha untuk membantu meningkatkan kesejahteraan ekonomi masyarakat.</p>
