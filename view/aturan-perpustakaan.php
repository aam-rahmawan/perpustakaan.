 <div class="page-head-line"><h2>Aturan Perpustakaan</h2></div>
 <p><strong><h3>A. PERSYARATAN</h3></strong></p>
 <p><strong><h4>A.I SYARAT MASUK</h4></strong></p>
 <p>1.Anggota Perpustakaan Daarul Fikri.</p>
 <p>2.Menitipkan semua barang bawaan kecuali barang berharga.</p>
 <p>3.Kepemilikan KTA sah.</p>
 <p><strong><h4>A.II SYARAT MASUK</h4></strong></p>
 <p>1.Penyelesaian jenis transaksi.</p>
 <p>2.Pemeriksaan barang bawan.</p>
 <p>3.Pemeriksaan lain bila dipandang perlu.</p>
 <p><strong><h3>B. LARANGAN</h3></strong></p>
 <p>1.Memutilasi bahan pustaka berupa buku ataupun non buku.</p>
 <p>2.Menimbulkan suara berisik dan gaduh.</p>
 <p>3.Mengatur sendiri susunan koleksi dengan segala cara.</p>
 <p>4.Membuang sampah sembarangan.</p>
 <p>5.Makan, minum (kecuali di area library lounge) dan merokok.</p>
 <p><strong><h3>C. ANJURAN</h3></strong></p>
 <p>1.Berpakaian dan Beretika yang sopan.</p>
 <p>2.Mengembalikan bahan pustaka tepat waktu.</p>
 <p>3.Responsif terhadap semua niat baik staf memberikan layanan.</p>
