 <div class="page-head-line"><h2>Profil Sekolah Daarul Fikri</h2></div>
<p>Daarul Fikri adalah sekolah formal, tingkat TK-SD dan SMP (full day : 07.30-16.00). Sebuah sekolah berbasis karakter yang mengangkat kecerdasan emosional, dan spiritual serta kepribadian untuk mencapai kemajuan dan kesejahteraan dunia akhirat. Mottonya: Pembinaan Insan Qurani, Kreatif dan Inovatif.
Siswa dibina agar menjadi pribadi yang unggul dalam berbagai segi, meneladani Rasulullah dalam berbagai aspek kehidupan, dibekali berbagai teori ilmu, dan diasah dengan berbagai latihan keterampilan hidup. Tujuannya, diharapkan mereka menjadi manusia yang cerdas dan terampil dalam mengambil keputusan yang tepat, menjadi pelopor dalam parubahan kehidupan yang lebih baik, memberikan banyak manfaat bagi lingkungannya. 
Suasana belajar yang alami, sejuk, dan kondusif, serta dibina oleh guru-guru yang profesional, penyabar dan penuh kasih sayang, membuat mereka merasa nyaman belajar, senang, selalu semangat belajar dan bermain di sekolah Daarul Fikri.
TK – SD Daarul Fikri: disingkat TK-SD DF didirikan oleh Yayasan Ar-Razaq, sejak  tahun ajaran 1999/ 2000 hingga sekarang, di Bandung Jawa Barat Indonesia. Kemudian pada tahun 2007/2008 mulai dikembangkan SMP Daarul Fikri.</p>
